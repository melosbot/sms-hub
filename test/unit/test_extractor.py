"""验证码提取语料库。每条都来自真实短信模式;改 KEYWORDS/CODE_RE 前先跑这里。"""
import pytest

from core.sms import extractor

CASES = [
    # ── 正常提取 ──
    ("【某站】验证码 114514,5 分钟内有效,请勿泄露。", "114514"),
    ("您正在进行网银转账,动态密码 4829,请在 2 分钟内输入。【工商银行】", "4829"),
    ("验证码:G4X9T2,请勿泄露给他人", "G4X9T2"),
    ("Your verification code is 552233", "552233"),
    ("[SomeApp] code: 9921, expires in 10 minutes", "9921"),
    ("您的驗證碼為 887766,請於 10 分鐘內輸入", "887766"),
    ("887766 是您的登录验证码", "887766"),          # 候选在关键词之前
    ("test code A1B2C3 ok", "A1B2C3"),              # 字母数字混合
    ("【平台】短信口令 73921,切勿告知他人", "73921"),
    ("验证码1234有效期5分钟", "1234"),              # 关键词与数字相连
    # ── 多候选时取关键词最近的 ──
    ("验证码 5566,订单尾号 7788", "5566"),
    ("验证码 1234,客服电话 4001234567", "1234"),    # 10 位客服号不是候选
    # ── 带连字符的验证码(命中后去 -)──
    ("验证码 AHD-SUC,请勿泄露给他人", "AHDSUC"),
    ("您的验证码 S1-FU-37,5 分钟内有效", "S1FU37"),
    ("验证码 ABCD-EFGH-12,请勿泄露给他人", None),   # 去 - 后 10 位,超 8 位不提取
    # ── 不应提取(无关键词/纯长数字) ──
    ("【菜鸟驿站】您的包裹已到站,凭取件码 5-2-8842 领取。", None),
    ("您账户支出 1000 元,余额 2345 元", None),
    ("您的订单20260612123456已发货,请注意查收", None),
    # ── 长数字串边界:手机号/订单号的前 8 位不能成为候选 ──
    ("验证码已发送至您的尾号13800138000手机", None),
    ("【某行】尾号8000的账户收入100元,详询客服13800138000", None),
]


@pytest.mark.parametrize("text,expected", CASES)
def test_extract_code(text, expected):
    assert extractor.extract_code(text) == expected
